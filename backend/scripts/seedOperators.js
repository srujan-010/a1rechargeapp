const seedOperators = async () => {
  try {
    await connectDB();
    console.log('Seeding Operators...');

    let displayOrder = 1;
    for (const op of operators) {
      await ProviderOperator.findOneAndUpdate(
        { provider: 'A1Topup', code: op.code },
        { 
          provider: 'A1Topup',
          name: op.name,
          code: op.code,
          serviceType: op.serviceType,
          plansInfoCode: op.plansInfoCode,
          displayOrder: displayOrder++
        },
        { upsert: true, new: true, setDefaultsOnInsert: true }
      );
    }
    
    console.log(`Successfully seeded ${operators.length} operators.`);
    process.exit(0);
  } catch (error) {
    console.error('Error seeding operators:', error);
    process.exit(1);
  }
};

